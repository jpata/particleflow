def ffn(input_dim, output_dim, width, dropout=0.1):
    return nn.Sequential(
        nn.Linear(input_dim, width),
        nn.GELU(),
        nn.LayerNorm(width),
        nn.Dropout(dropout),
        nn.Linear(width, output_dim),
    )


class PositionalEncoding(nn.Module):
    def __init__(self, embedding_dim):
        super(PositionalEncoding, self).__init__()
        self.proj = nn.Linear(2, embedding_dim)

    def forward(self, x, X):
        # Extract eta and phi
        eta = X[..., 2:3]
        phi = torch.atan2(X[..., 3:4], X[..., 4:5])
        coords = torch.cat([eta, phi], dim=-1)  # [B, N, 2]
        return x + self.proj(coords)


class RegressionOutput(nn.Module):
    def __init__(self, mode, embed_dim, width, elemtypes, dropout=0.1):
        super(RegressionOutput, self).__init__()
        self.mode = mode  # "direct", "additive", "multiplicative", "linear"
        self.embed_dim = embed_dim
        self.width = width
        self.dropout_p = dropout
        self.elemtypes = elemtypes
        if mode == "linear":
            self.nn = ffn(embed_dim, 2 * len(elemtypes), width, dropout=dropout)
        else:
            self.nn = ffn(embed_dim, len(elemtypes), width, dropout=dropout)

    def extra_repr(self):
        return f"mode={self.mode}, embed_dim={self.embed_dim}, width={self.width}, dropout={self.dropout_p}"

    def forward(self, X, x, orig_value):
        # X: [B, N, 25] or [B, N, 55] (original features)
        # x: [B, N, D] (latent representation)
        # orig_value: [B, N, 1] (the feature to regress from)

        nn_out = self.nn(x)
        elemtype_mask = torch.stack([X[..., 0] == elemtype for elemtype in self.elemtypes], dim=-1)

        if self.mode == "linear":
            # nn_out: [B, N, 2 * len(elemtypes)]
            num_types = len(self.elemtypes)
            a_all = nn_out[..., :num_types]
            b_all = nn_out[..., num_types:]
            a = torch.sum(elemtype_mask * a_all, dim=-1, keepdim=True)
            b = torch.sum(elemtype_mask * b_all, dim=-1, keepdim=True)
            return orig_value * a + b
        else:
            res = torch.sum(elemtype_mask * nn_out, dim=-1, keepdim=True)
            if self.mode == "direct":
                return res
            elif self.mode == "additive":
                return orig_value + res
            elif self.mode == "multiplicative":
                return orig_value * res
            else:
                return res


class MLPF(nn.Module):
    def __init__(self, config=None, **kwargs):
        super(MLPF, self).__init__()

        if config is None:
            # Backward compatibility or manual construction
            from mlpf.standalone.dsl import i, h, g, s, f, o, ModelConfig

            input_dim = kwargs.get("input_dim", 55)
            embedding_dim = kwargs.get("embedding_dim", 128)
            width = kwargs.get("width", 128)
            num_convs = kwargs.get("num_convs", 6)
            num_heads = kwargs.get("num_heads", 16)
            attention_type = kwargs.get("attention_type", "global")

            if attention_type == "hept":
                layer = h(num_heads, embedding_dim, width * 4)
            elif attention_type == "standard":
                layer = s(num_heads, embedding_dim, width * 4)
            elif attention_type == "fastformer":
                layer = f(num_heads, embedding_dim, width * 4)
            else:
                layer = g(num_heads, embedding_dim, width * 4)

            config = ModelConfig(
                input=i(input_dim, embedding_dim, width * 2),
                backbone={"shared": layer * num_convs},
                output=o(kwargs.get("num_classes", 8), width * 2),
            )

        self.config = config
        self.elemtypes = [1, 2, 3, 4, 5, 8, 9, 10, 11]

        # 1. Input encoding
        if config.input.type == "default":
            self.nn0 = ffn(config.input.input_dim, config.input.embedding_dim, config.input.width, dropout=config.input.dropout)
            self.type_emb = nn.Embedding(12, config.input.embedding_dim)
        elif config.input.type == "projection_only":
            self.nn0 = nn.Linear(config.input.input_dim, config.input.embedding_dim)
        elif config.input.type == "advanced":
            self.nn0 = ffn(config.input.input_dim, config.input.embedding_dim, config.input.width, dropout=config.input.dropout)
            self.type_emb = nn.Embedding(12, config.input.embedding_dim)
            self.coord_proj = nn.Linear(2, config.input.embedding_dim)

        # 2. Backbone
        self.backbone = nn.ModuleDict()
        for k, layers in config.backbone.items():
            self.backbone[k] = nn.ModuleList()
            for layer_cfg in layers:
                layer_class = SUPPORTED_ATTENTION_LAYERS[layer_cfg.type]
                self.backbone[k].append(layer_class(**layer_cfg.__dict__))

        # 3. Output heads
        decoding_dim = config.input.embedding_dim
        self.nn_id = ffn(decoding_dim, config.output.num_classes, config.output.width, dropout=config.output.dropout)

        rg_mode = config.output.rg_mode
        self.reg_heads = nn.ModuleDict()
        for feat in ["pt", "eta", "sin_phi", "cos_phi", "energy"]:
            mode = rg_mode[feat] if isinstance(rg_mode, dict) else rg_mode
            self.reg_heads[feat] = RegressionOutput(mode, decoding_dim, config.output.width, self.elemtypes, dropout=config.output.dropout)

    def forward(self, X, mask, return_attn=False):
        # X: [B, N, 55]
        # mask: [B, N]
        if mask is not None:
            mask_ = mask.unsqueeze(-1)

        # 1. Encoding
        if self.config.input.type == "default":
            x = self.nn0(X)
            x = x + self.type_emb(X[..., 0].long())
        elif self.config.input.type == "projection_only":
            x = self.nn0(X)
        elif self.config.input.type == "advanced":
            x = self.nn0(X)
            x = x + self.type_emb(X[..., 0].long())
            eta_phi = X[..., 2:4]
            x = x + self.coord_proj(eta_phi)

        if mask is not None:
            x = x * mask_

        # 2. Backbone
        attns = []
        if "shared" in self.backbone:
            for layer in self.backbone["shared"]:
                if return_attn:
                    x, attn = layer(x, mask, X, return_attn=True)
                    attns.append(attn)
                else:
                    x = layer(x, mask, X)

        # 3. Output
        # PID
        x_id = x
        if "pid" in self.backbone:
            for layer in self.backbone["pid"]:
                x_id = layer(x_id, mask, X)
        preds_id = self.nn_id(x_id)

        # Regression
        x_reg = x
        if "reg" in self.backbone:
            for layer in self.backbone["reg"]:
                x_reg = layer(x_reg, mask, X)

        preds_pt = self.reg_heads["pt"](X, x_reg, X[..., 1:2])
        preds_eta = self.reg_heads["eta"](X, x_reg, X[..., 2:3])
        preds_sin_phi = self.reg_heads["sin_phi"](X, x_reg, X[..., 3:4])
        preds_cos_phi = self.reg_heads["cos_phi"](X, x_reg, X[..., 4:5])
        preds_energy = self.reg_heads["energy"](X, x_reg, X[..., 5:6])

        preds_momentum = torch.cat([preds_pt, preds_eta, preds_sin_phi, preds_cos_phi, preds_energy], dim=-1)

        # Placeholder for binary particle prediction (legacy)
        preds_binary = torch.zeros((x.shape[0], x.shape[1], 2), device=x.device)

        if return_attn:
            return preds_binary, preds_id, preds_momentum, attns
        return preds_binary, preds_id, preds_momentum, None


def train(model, loader, optimizer, device):
    model.train()
    total_loss = 0
    for i, batch in enumerate(loader):
        X = batch.X.to(device)
        y = batch.y.to(device)
        mask = batch.mask.to(device)

        optimizer.zero_grad()
        _, preds_id, preds_momentum, _ = model(X, mask)

        # Simple cross-entropy for PID
        # preds_id: [B, N, num_classes]
        # target_id: y[:, :, 0]
        loss_id = F.cross_entropy(preds_id.reshape(-1, preds_id.shape[-1]), y[:, :, 0].long().reshape(-1), reduction="none")
        loss_id = (loss_id * mask.reshape(-1)).sum() / mask.sum()

        # Simple MSE for momentum
        # preds_momentum: [B, N, 5]
        # target_momentum: y[:, :, 2:7]
        loss_reg = F.mse_loss(preds_momentum, y[:, :, 2:7], reduction="none")
        loss_reg = (loss_reg.sum(dim=-1) * mask).sum() / mask.sum()

        loss = loss_id + loss_reg
        loss.backward()
        optimizer.step()

        total_loss += loss.item()
        if i % 10 == 0:
            print(f"  Step {i}, Loss: {loss.item():.4f} (ID: {loss_id.item():.4f}, Reg: {loss_reg.item():.4f})")

    return total_loss / len(loader)


@torch.no_grad()
def validate(model, loader, device):
    model.eval()
    total_loss = 0
    for batch in loader:
        X = batch.X.to(device)
        y = batch.y.to(device)
        mask = batch.mask.to(device)

        _, preds_id, preds_momentum, _ = model(X, mask)

        loss_id = F.cross_entropy(preds_id.reshape(-1, preds_id.shape[-1]), y[:, :, 0].long().reshape(-1), reduction="none")
        loss_id = (loss_id * mask.reshape(-1)).sum() / mask.sum()

        loss_reg = F.mse_loss(preds_momentum, y[:, :, 2:7], reduction="none")
        loss_reg = (loss_reg.sum(dim=-1) * mask).sum() / mask.sum()

        total_loss += (loss_id + loss_reg).item()

    return total_loss / len(loader)
