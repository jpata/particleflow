class HEPTAttentionLayer(nn.Module):
    def __init__(self, embedding_dim, num_heads, width, dropout=0.1, pos=False, **kwargs):
        super(HEPTAttentionLayer, self).__init__()
        self.embedding_dim = embedding_dim
        self.num_heads = num_heads
        self.width = width
        self.dropout_p = dropout
        self.pos = pos

        hept_conf = {k: v for k, v in kwargs.items() if k in ["block_size", "n_hashes", "num_regions", "num_w_per_dist"]}
        self.mha = HEPTAttention(embedding_dim, num_heads, **hept_conf)
        self.norm0 = nn.LayerNorm(embedding_dim)
        self.norm1 = nn.LayerNorm(embedding_dim)
        self.seq = nn.Sequential(nn.Linear(embedding_dim, width), nn.GELU(), nn.Linear(width, embedding_dim), nn.GELU())
        self.dropout = nn.Dropout(dropout)
        self.pos_embed = PositionalEncoding(embedding_dim) if pos else None

    def extra_repr(self):
        return f"embedding_dim={self.embedding_dim}, num_heads={self.num_heads}, width={self.width}, dropout={self.dropout_p}, pos={self.pos}"

    def forward(self, x, mask, X=None, return_attn=False):
        # x: [B, N, D]
        # mask: [B, N] (valid entries)
        # X: [B, N, 55] (original features for coordinates)
        if self.pos_embed and X is not None:
            x = self.pos_embed(x, X)

        if mask is not None:
            mask_ = mask.unsqueeze(-1)

        residual = x
        x_norm = self.norm0(x)
        if mask is not None:
            x_norm = x_norm * mask_

        # Use eta, phi for partitioning [B, N, 2]
        coords = X[..., 2:4] if X is not None else torch.zeros((x.shape[0], x.shape[1], 2), device=x.device)
        mha_out = self.mha(x_norm, mask, coords)
        x = residual + mha_out

        residual = x
        x_norm = self.norm1(x)
        ffn_out = self.seq(x_norm)
        ffn_out = self.dropout(ffn_out)

        x = residual + ffn_out
        if mask is not None:
            x = x * mask_

        if return_attn:
            return x, None  # HEPT sparse attention matrix not easily reconstructed for full N
        return x


